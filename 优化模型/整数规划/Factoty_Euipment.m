%% 用蒙特卡洛解决工厂分配设备问题
clear;clc;
n = 10000; %模拟次数
C = [4,2,3,4;
     6,4,5,5;
     7,6,7,6;
     7,8,8,6;
     7,9,8,6;
     7,10,8,6];
 res_x = 0;
 res = 0;
 for k = 1:n
     flag = 1;
     x = randi([1,4],1,6); %生成一个1*6的向量，元素为1-4的整数，表示该设备分配给了哪个企业
     for j = 1 : 4 %检查每个企业至少分配一台
         
         if(ismember(j,x) == 0)
             flag = 0;
             break;
         end
     end
     
     if(flag == 1)
         sum = 0;
         for j = 1 : 6
             sum = sum + C(j, x(j));
         end
         
         if(sum > res)
             res = sum;
             res_x = x;
         end
     end
 end

